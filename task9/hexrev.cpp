void to_hex_num(BYTE* buf, size_t buf_size)
{
	if (buf_size == 0) return;
	for (size_t i = buf_size - 1; i != 0; i--) {
		printf("%02x", (unsigned)buf[i]);
	}
	printf("%02x", (unsigned)buf[0]);
	printf("\n");
}


